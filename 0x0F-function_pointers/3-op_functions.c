#include "3-calc.h"

/**
 * op_add - adds
 * @a: the first
 * @b: the second
 *
 * Return: the sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subs
 * @a: the first
 * @b: the second
 *
 * Return: the sum
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - muls
 * @a: the first
 * @b: the second
 *
 * Return: the sum
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divs
 * @a: the first
 * @b: the second
 *
 * Return: the sum
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mods
 * @a: the first
 * @b: the second
 *
 * Return: the sums
*/

int op_mod(int a, int b)
{
	return (a % b);
}
